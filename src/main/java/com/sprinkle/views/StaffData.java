   private void searchAdminsData(String keyword) {
    // Skip search if it's the placeholder text
    if (keyword.equals(" Search staffs...") || keyword.trim().isEmpty()) {
        loadAdminsData();
        return;
    }

    AdminController adminController = new AdminController();
    List<AdminModel> admins = adminController.searchStaffs(keyword);

    String[] columnNames = {
        "Admin ID", "Username", "Name", "Phone", "Email", "Role"
    };

    DefaultTableModel model = new DefaultTableModel(columnNames, 0);

    for (AdminModel admin : admins) {
        Object[] rowData = {
            admin.getAdminId(),
            admin.getUsername(),
            admin.getName(),
            admin.getPhone(),
            admin.getEmail(),
            admin.getRole()
        };
        model.addRow(rowData);
    }

    jTable1.setModel(model);
}

private void loadAdminsData() {
    AdminController adminController = new AdminController();
    List<AdminModel> admins = adminController.getAllStaffs();

    String[] columnNames = {
        "Admin ID", "Username", "Name", "Phone", "Email", "Role"
    };

    DefaultTableModel model = new DefaultTableModel(columnNames, 0);

    for (AdminModel admin : admins) {
        Object[] rowData = {
            admin.getAdminId(),
            admin.getUsername(),
            admin.getName(),
            admin.getPhone(),
            admin.getEmail(),
            admin.getRole()
        };
        model.addRow(rowData);
    }

    jTable1.setModel(model);
}




    
