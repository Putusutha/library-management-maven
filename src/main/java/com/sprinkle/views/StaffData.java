    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        // Delete button action
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a admin to delete.");
            return;
        }
//        int adminId = (int) jTable1.getValueAt(selectedRow, 0);

        int confirm = JOptionPane.showConfirmDialog(this,
                "Are you sure you want to delete this staff?",
                "Confirm Delete",
                JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            int adminId = (int) jTable1.getValueAt(selectedRow, 0);
            AdminController adminController = new AdminController();
            boolean success = adminController.delete(adminId);
            if (success) {
                JOptionPane.showMessageDialog(this, "Staff deleted successfully.");
            } else {
                JOptionPane.showMessageDialog(this, "Failed to delete admin.");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

