private void searchBooksData(String keyword) {
    BookController bookController = new BookController();
    List<BookModel> books = bookController.searchBooks(keyword);

    String[] columnNames = {
        "Book ID", "Title", "Author", "Publisher", "Year", "ISBN",
        "Category ID", "Qty", "Available Qty"
    };

    DefaultTableModel model = new DefaultTableModel(columnNames, 0);

    for (BookModel book : books) {
        Object[] rowData = {
            book.getBookId(),
            book.getTitle(),
            book.getAuthor(),
            book.getPublisher(),
            book.getYearPublished(),
            book.getIsbn(),
            book.getCategoryId(),
            book.getTotalQuantity(),
            book.getAvailableQuantity()
        };
        model.addRow(rowData);
    }

    jTable1.setModel(model);
}




