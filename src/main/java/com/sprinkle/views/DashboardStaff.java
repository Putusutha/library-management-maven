    private void loadRecentReturnedBooks() {
        List<LoanModel> loans = loanController.getRecentReturnedBooks();
        tableModel.setRowCount(0); // Reset data tabel sebelum diisi ulang

        for (LoanModel loan : loans) {
            tableModel.addRow(new Object[]{
                "SP/LOAN/" + loan.getLoanId(), // Format Loan ID
                loan.getMemberName(),
                loan.getBookTitle(),
                loan.getCategoryName(),
                loan.getAdminName(),
                loan.getReturnDate()
            }
        );
        }

    }
    
    private void updateDashboardMetrics() {
        int[] metrics = dashboardController.getDashboardMetrics();

        lblTotalMembers.setText(metrics[0]+" members");
        lblTotalLoans.setText(metrics[1]+" books");
        lblTotalFines.setText("Rp " + metrics[2]); // Format Rupiah
        lblTotalBooks.setText(metrics[3]+" books");
        
    }
